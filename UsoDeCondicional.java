// uso de if e else 
class UsoDeCondicional {

    public static void main(String[] args) {
      int x = 20;
      int y = 22;

      if (x > y) {
          System.out.println("O valor de X é maior do que o valor de Y");
      }
      else {
          System.out.println("O valor de Y é maior do que o valor de X");
      }
    }

  }